            <div class="article-content">
                <p>Untuk mengurangi beban masyarakat, pemerintah telah meluncurkan program subsidi minyak goreng. Harga minyak goreng kini berkisar di Rp14.000 per liter, jauh lebih rendah dibandingkan sebelumnya. Kebijakan ini bertujuan untuk membantu keluarga miskin dan menengah agar dapat memenuhi kebutuhan dasar dengan harga yang lebih terjangkau.</p>
                <p>Program subsidi ini mendapat respon positif dari masyarakat, khususnya para ibu rumah tangga yang merasakan dampak langsung dari kebijakan tersebut. "Kami merasa lebih ringan dengan adanya subsidi ini, harga minyak goreng yang sempat melambung kini kembali stabil," ujar Linda, seorang ibu rumah tangga di Jakarta.</p>
                <p>Selain itu, pemerintah juga memastikan bahwa distribusi minyak goreng berjalan lancar dengan memantau stok dan distribusinya secara real-time. Langkah ini diharapkan dapat menghindari kelangkaan dan meminimalkan potensi spekulasi harga yang merugikan konsumen.</p>
                <p>Beberapa pedagang pasar tradisional juga menyambut baik kebijakan ini. Mereka mengaku harga minyak goreng yang terjangkau dapat meningkatkan daya beli masyarakat, sehingga mendorong penjualan barang lainnya. "Penjualan kami lebih lancar, karena banyak yang membeli minyak goreng untuk kebutuhan rumah tangga," kata salah satu pedagang di Pasar Induk.</p>
